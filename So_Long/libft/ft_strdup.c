#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	int		i;
	int		lenght;

	lenght = ft_strlen(str);
	i = 0;
	dup = malloc(sizeof(char) * lenght + 1);
	if (!dup)
		return (0);
	while (i < lenght)
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
